def clean_text(text: str) -> str:
    if not text:
        return ''
    text = text.replace('\x00', '')
    text = text.replace('\r\n', '\n').replace('\r', '\n')
    text = re.sub(r'[\x01-\x08\x0b\x0c\x0e-\x1f\x7f]', '', text)
    text = '\n'.join(line.rstrip() for line in text.split('\n'))
    text = re.sub(r'\n{4,}', '\n\n\n', text)
    return text.strip()


def normalize_math_ocr_text(text: str) -> str:
    """Light cleanup only. Avoid aggressive replacements that could change math meaning."""
    if not text:
        return ''
    text = clean_text(text)
    # Normalize common OCR dashes/minus signs to ASCII hyphen inside searchable text.
    text = text.replace('−', '-').replace('—', '-')
    # Common OCR quotes/dots cleanup.
    text = text.replace('“', '"').replace('”', '"').replace('‘', "'").replace('’', "'")
    # Join obvious broken page divider noise from Tesseract, but keep content lines.
    lines = []
    for line in text.split('\n'):
        stripped = line.strip()
        # Drop long fake separator lines caused by page border scanning.
        if len(stripped) > 25 and set(stripped) <= set('-_=~Ss. *'):
            continue
        lines.append(line.rstrip())
    return re.sub(r'\n{4,}', '\n\n\n', '\n'.join(lines)).strip()


def pdf_page_from_printed(printed_page: int) -> int:
    return printed_page + PRINTED_OFFSET


def printed_page_from_pdf(pdf_page: int) -> Optional[int]:
    return pdf_page - PRINTED_OFFSET if pdf_page >= 8 else None


def printed_label_from_pdf(pdf_page: int) -> Optional[str]:
    p = printed_page_from_pdf(pdf_page)
    if p is not None:
        return str(p)
    return ROMAN_LABELS.get(pdf_page)


def build_chapter_ranges(total_pdf_pages: int) -> List[dict]:
    items = []
    for i, ch in enumerate(CHAPTERS):
        start_printed = ch['printed_start_page']
        if i < len(CHAPTERS) - 1:
            end_printed = CHAPTERS[i + 1]['printed_start_page'] - 1
        else:
            end_printed = total_pdf_pages - PRINTED_OFFSET
        start_pdf = pdf_page_from_printed(start_printed)
        end_pdf = pdf_page_from_printed(end_printed)
        chapter_type = ch.get('chapter_type', 'chapter')
        items.append({
            **ch,
            'chapter_type': chapter_type,
            'start_page': start_pdf,
            'end_page': end_pdf,
            'printed_end_page': end_printed,
        })
    return items


def find_chapter_for_pdf_page(pdf_page: int, ranges: List[dict]) -> Optional[dict]:
    for ch in ranges:
        if ch['start_page'] <= pdf_page <= ch['end_page']:
            return ch
    return None


def classify_front_page(pdf_page: int, text: str) -> str:
    t = (text or '').lower()
    if not t:
        return 'blank'
    if pdf_page == 1:
        return 'cover'
    if pdf_page == 2:
        return 'title_page'
    if pdf_page == 3:
        return 'copyright_page'
    if 'preface' in t:
        return 'preface'
    if 'contents' in t and 'integers' in t:
        return 'toc'
    if 'covid' in t or 'pandemic' in t or 'content load' in t:
        return 'syllabus_reduction_note'
    return 'front_matter'


def ocr_quality_flags(text: str, selectable_text: str = '') -> Tuple[List[str], Dict[str, int]]:
    flags: List[str] = []
    suspicious_chars = SUSPICIOUS_UNICODE_RE.findall(text or '')
    bad_pdf_chars = sum((selectable_text or '').count(ch) for ch in BAD_PDF_OCR_CHARS)
    metrics = {
        'text_length': len(text or ''),
        'suspicious_unicode_count': len(suspicious_chars),
        'bad_pdf_ocr_char_count_in_selectable_layer': bad_pdf_chars,
        'math_line_count': len(extract_math_lines(text or '')),
    }
    if not (text or '').strip():
        flags.append('blank_or_no_ocr_text_detected')
    if len(suspicious_chars) > 0:
        flags.append('suspicious_unicode_in_ocr_text')
    if bad_pdf_chars > 0:
        flags.append('pdf_text_layer_had_corrupt_math_ocr_chars')
    if metrics['math_line_count'] > 8:
        flags.append('math_dense_page')
    # This book is scanned and math-heavy, so exact math should still be reviewed for high-stakes use.
    flags.append('rendered_page_ocr_used_for_math_text')
    return flags, metrics


def extract_math_lines(text: str) -> List[str]:
    out: List[str] = []
    for raw in (text or '').split('\n'):
        line = raw.strip()
        if len(line) < 3:
            continue
        if MATH_LINE_RE.search(line):
            out.append(line)
    return out[:80]  # prevent huge duplication in JSON


def extract_blocks(text: str) -> List[dict]:
    """Simple lightweight block markers for RAG metadata; not a full parser."""
    blocks: List[dict] = []
    current = None
    for line in (text or '').split('\n'):
        s = line.strip()
        if not s:
            continue
        m = HEADING_RE.match(s)
        if m:
            if current:
                current['text'] = '\n'.join(current['lines']).strip()
                del current['lines']
                blocks.append(current)
            title = m.group(1)
            block_type = 'example' if title.upper().startswith('EXAMPLE') else 'exercise' if title.upper().startswith('EXERCISE') else 'rule' if title.lower().startswith('rule') else 'section_marker'
            current = {'block_type': block_type, 'title': title, 'lines': [s]}
        elif current:
            current['lines'].append(s)
    if current:
        current['text'] = '\n'.join(current['lines']).strip()
        del current['lines']
        blocks.append(current)
    return blocks[:40]


def render_and_ocr_page(pdf_path: str, page_number: int) -> dict:
    """Worker: render a 1-based PDF page to image and OCR it with Tesseract."""
    OCR_CACHE_DIR.mkdir(parents=True, exist_ok=True)
    # Prefer high-resolution OCR cache when available; fall back to full fast cache.
    high_cache_file = HIGH_RES_OCR_CACHE_DIR / f'page_{page_number:04d}_psm{TESSERACT_PSM}_scale2_0.txt'
    cache_file = OCR_CACHE_DIR / f'page_{page_number:04d}.txt'
    if high_cache_file.exists():
        ocr = high_cache_file.read_text(encoding='utf-8', errors='replace')
        return {'page_number': page_number, 'ocr_text_raw': ocr, 'from_cache': True, 'cache_profile': 'high_res_scale_2_0'}
    if cache_file.exists():
        ocr = cache_file.read_text(encoding='utf-8', errors='replace')
        return {'page_number': page_number, 'ocr_text_raw': ocr, 'from_cache': True, 'cache_profile': 'fast_scale_1_2'}

    doc = fitz.open(pdf_path)
    page = doc.load_page(page_number - 1)
    pix = page.get_pixmap(matrix=fitz.Matrix(RENDER_SCALE, RENDER_SCALE), alpha=False)
    with tempfile.NamedTemporaryFile(suffix='.png', delete=False) as tmp:
        tmp_path = tmp.name
    try:
        pix.save(tmp_path)
        env = os.environ.copy()
        env['OMP_THREAD_LIMIT'] = '1'
        cmd = [
            'tesseract', tmp_path, 'stdout',
            '--psm', TESSERACT_PSM,
            '-l', TESSERACT_LANG,
            '-c', 'preserve_interword_spaces=1',
        ]
        proc = subprocess.run(
            cmd,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=False,
            env=env,
            timeout=60,
        )
        stdout_text = (proc.stdout or b'').decode('utf-8', errors='replace')
        stderr_text = (proc.stderr or b'').decode('utf-8', errors='replace')
        if proc.returncode != 0:
            ocr = ''
            err = stderr_text.strip()[:1000]
        else:
            ocr = stdout_text or ''
            err = ''
        cache_file.write_text(ocr, encoding='utf-8', errors='replace')
        return {'page_number': page_number, 'ocr_text_raw': ocr, 'from_cache': False, 'error': err}
    finally:
        try:
            os.remove(tmp_path)
        except OSError:
            pass


def format_page_block(page_record: dict) -> str:
    pp = page_record.get('printed_page_number')
    pp_label = pp if pp is not None else page_record.get('printed_page_label')
    return f"[PDF page {page_record['page_number']} / printed page {pp_label}]\n{page_record.get('text','').strip()}".strip()


