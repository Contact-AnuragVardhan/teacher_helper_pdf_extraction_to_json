EXERCISE_TOKEN_RE = re.compile(r'\bEXERCISE\s+([0-9A-Za-z¢©]{1,5})', re.IGNORECASE)
ACTIVITY_TOKEN_RE = re.compile(r'\bACTIVITY\s+([0-9]{1,2})', re.IGNORECASE)
OCR_LETTER_FIXES = {
    '¢': 'C', '©': 'C', '0': 'O',
    # OCR sometimes reads B as 8, especially in Exercise 1B-style headings.
    '8': 'B',
}


def _normalize_marker_token(token: str) -> str:
    token = (token or '').strip().upper()
    token = token.replace('¢', 'C').replace('©', 'C')
    token = token.replace('I', '1').replace('L', '1')
    token = token.replace('O', '0')
    return token


def _normalize_exercise_marker(raw_token: str, chapter_number: str) -> str | None:
    """Return canonical labels like 'Exercise 1A' or 'Exercise 13'."""
    if not chapter_number or not str(chapter_number).isdigit():
        return None
    chapter_number = str(chapter_number)
    token = _normalize_marker_token(raw_token)
    if not token:
        return None

    # Normal case: token starts with the chapter number, e.g. 20F.
    if token.startswith(chapter_number):
        suffix = token[len(chapter_number):]
        suffix = ''.join(OCR_LETTER_FIXES.get(ch, ch) for ch in suffix)
        suffix = re.sub(r'[^A-Z]', '', suffix)
        return f'Exercise {chapter_number}{suffix}' if suffix else f'Exercise {chapter_number}'

    # Common OCR case for chapter 1: '1B' can be read as just '8'.
    if len(token) == 1 and chapter_number == '1' and token in OCR_LETTER_FIXES:
        fixed = OCR_LETTER_FIXES[token]
        if fixed.isalpha():
            return f'Exercise {chapter_number}{fixed}'

    # If the token is only a letter, attach it to the current chapter.
    if len(token) == 1 and token.isalpha():
        return f'Exercise {chapter_number}{token}'

    return None


def detect_subsection_markers(chapter: dict, pages: List[dict]) -> List[dict]:
    """Detect exercise/activity anchors inside a chapter from OCR page text."""
    chapter_number = str(chapter.get('chapter_number'))
    chapter_title = chapter.get('chapter_title') or ''
    markers: List[dict] = []
    seen = set()

    is_activity_chapter = chapter_number == '24' or chapter_title.lower() == 'activities'
    for page in pages:
        text = page.get('text') or page.get('text_plain') or ''
        if not text:
            continue

        if is_activity_chapter:
            for m in ACTIVITY_TOKEN_RE.finditer(text):
                marker = f"Activity {int(m.group(1))}"
                if marker in seen:
                    continue
                seen.add(marker)
                markers.append({
                    'marker': marker,
                    'kind': 'activity',
                    'page_number': page.get('page_number'),
                    'printed_page_number': page.get('printed_page_number'),
                    'order': m.start(),
                })
            continue

        for m in EXERCISE_TOKEN_RE.finditer(text):
            marker = _normalize_exercise_marker(m.group(1), chapter_number)
            if not marker or marker in seen:
                continue
            seen.add(marker)
            markers.append({
                'marker': marker,
                'kind': 'exercise',
                'page_number': page.get('page_number'),
                'printed_page_number': page.get('printed_page_number'),
                'order': m.start(),
            })

    markers.sort(key=lambda x: (int(x.get('page_number') or 0), int(x.get('order') or 0), x.get('marker') or ''))
    return markers


def group_subsection_markers(chapter: dict, markers: List[dict]) -> List[dict]:
    """Group immediate/objective tail exercises with the previous subsection."""
    chapter_number = str(chapter.get('chapter_number'))
    trailing_group = TRAILING_EXERCISE_GROUPS_BY_CHAPTER.get(chapter_number, set())
    groups: List[dict] = []
    for marker in markers:
        label = marker['marker']
        if label in trailing_group and groups:
            groups[-1]['included_markers'].append(label)
            groups[-1]['included_marker_details'].append(marker)
            groups[-1]['notes'].append('Objective/immediate exercise grouped with the preceding concept subsection.')
            continue
        groups.append({
            'anchor': marker,
            'included_markers': [label],
            'included_marker_details': [marker],
            'notes': [],
        })
    return groups


