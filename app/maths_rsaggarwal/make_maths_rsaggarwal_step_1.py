    expected = list(range(8, total_pdf_pages + 1))
    missing = sorted(set(expected) - set(covered))
    extra = sorted(set(covered) - set(expected))
    if missing:
        errors.append(f"Missing content/answer pages from chapter mapping: {missing[:20]}{'...' if len(missing)>20 else ''}")
    if extra:
        errors.append(f"Chapter mapping contains pages outside expected content range: {extra[:20]}{'...' if len(extra)>20 else ''}")
    dupes = sorted({p for p in covered if covered.count(p) > 1})
    if dupes:
        errors.append(f"Duplicate chapter page assignments: {dupes[:20]}{'...' if len(dupes)>20 else ''}")
    if empty_pages:
        warnings.append(f"Pages with no final OCR text: {empty_pages}")
    if suspicious_pages:
        warnings.append(f"Pages with suspicious Unicode in final OCR text: {suspicious_pages[:50]}{'...' if len(suspicious_pages)>50 else ''}")
